grn = float(input("Введи суму в гривнях: "))        # вводимо суму в гривнях
usd = float(input("Введи курс долара: "))       # вводимо курс долара

ost = grn / usd          # ділимо гривні на курс → отримуємо долари

print("Привіт")
print("В тебе є", grn, "грн")
print("Це значить, що в тебе", round(ost, 2), "доларів")      # округляємо до 2 знаків