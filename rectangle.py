width = 12   # Ширина прямокутника (см)
height = 5   # Висота прямокутника (см)

area = width * height  # Обчислення площі
perimeter = 2 * (width + height)  # Обчислення периметра

print("=" * 30)  # Верхня рамка
print("   ЗВІТ ПРО ПРЯМОКУТНИК")  # Заголовок
print("=" * 30)  # Розділювач

print("Ширина:", width, "см")  # Вивід ширини
print("Висота:", height, "см")  # Вивід висоти
print("Площа:", area, "см²")  # Вивід площі
print("Периметр:", perimeter, "см")  # Вивід периметра

print("=" * 30)  # Нижня рамка